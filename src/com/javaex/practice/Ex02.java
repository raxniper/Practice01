package com.javaex.practice;

public class Ex02 {

	public static void main(String[] args) {
		System.out.println(12 / 5 - 3);		// 예상 : 12 / 5 - 3 = (12/5)-3 = 2.4-3 = -0.6
											// 정답 : -1 (소수점 이하는 버리고 계산한 걸로 결과가 나옴)
		System.out.println(5 + 19 % 3);		// 예상 : 5 + 19 % 3 = 5+(19%3) = 5+1 = 6
											// 정답 : 6 (예상과 일치)

	}

}
